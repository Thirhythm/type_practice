/// A titled test passage.
class TestPassage {
  final String title;
  final String author;
  final String text;

  const TestPassage({
    required this.title,
    required this.author,
    required this.text,
  });

  String get preview =>
      text.length > 50 ? '${text.substring(0, 50)}…' : text;

  int get charCount => text.characters.length;
}

